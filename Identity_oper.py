#Identity Operator (is or is not)
print("This Identity Operator is used to check the values or type are identitical or not.")
print("x=17\ny='hello'")
x=17
y="hello"
print("Now we check the type of x is string")
print(type(x) is str)
print("Now we check the type of x is integer")
print(type(x) is int)
print("Now we check the type of x is not type of y")
print(type(x) is not type(y))
print("Now we check the type of y is integer")
print(type(y) is int)
print("Now we check the type of y is not integer")
print(type(y) is not int)

