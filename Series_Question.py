print("\tSeries 1")
for a in range(1,6):
    for b in range(1,a):
        print(b ,end=" ")
    print("\n")
print("\n\n\t Series 2")
n=1
for c in range(1,6):
    for d in range(1,c):
        print(n,end=" ")
        n=n+1
    print("\n")
print("\n\n\t Series 3")
for i in range(1,6):
        for k in range(1,i):
            print("*",end=" ")
        print("\n")
print("\n\n\t Series 4")
for e in range(1,5):
    for f in range(4,0,-1):
        print(" ")
    for g in range(1,5):
        print(" $ ",end=" ")
    print("\n")
